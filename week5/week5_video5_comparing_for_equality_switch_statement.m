    case 1
        dayText='Monday';
    case 2    
        dayText='Tuesday';
    case 3
        dayText='Wednesday';
    case 4
        dayText='Thursday';
    case 5
        dayText='Friday';
    case 6
        dayText='Saturday';
    case 7
        dayText='Sun    day';
end